<p>
    في هذه الصفحة، يمكنك تصفح جميع الحجوزات. في الجدول، ستجد إسم الحجز، إسم مقدم الطلب، إسم اللجنة، نوع الحجز. وحالة الحجز (الطلب مرسل، مقبول، مرفوض، معدل) كما يمكنك النقر على رابط "المزيد" لرؤية الحجز كاملاً والموافقة عليه، رفضه أو تعديله.
</p>
<p>
    يمكنك أيضا البحث في هذا الجدول للعثور على طلب معين، كما يمكنك تحديد نوع الطلبات الذي تريد رؤيتها من لائحة الخيارات في الأعلى جهة الشمال.
</p>
