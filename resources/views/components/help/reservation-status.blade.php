<h2><u>موافق عليه</u></h2>
<p>
    الحجز موافق عليه من قبل المشرف
</p>
<hr />
<h2><u>الطلب مرسل</u></h2>
<p>
    تم إرسال الطلب الى المشرف لمراجعة الحجز واتخاذ قرار الموافقة عليه أو رفضه.
</p>
<hr />
<h2><u>متوقف</u></h2>
<p>
    الحجز متوقف لوقت مؤقت. يمكن رؤية فترة توقف الحجز في صفحة الحجز. بعد إنتهاء فترة الحجز، تتحول حالة الحجز ل(موافق عليه).
</p>
<hr />
<h2><u>ملغي</u></h2>
<p>
    الحجز ملغي من قبل المشرف
</p>
<hr />
<h2><u>مرفوض</u></h2>
<p>
    طلب الحجز لم يقبله المشرف.
</p>